#include "../include/minishell.h"

int	is_quote(char c)
{
	return (c == '"' || c == '\'');
}

int	is_operator(char c)
{
	return (c == '|' || c == '&' || c == '<' || c == '>'
		|| c == '(' || c == ')');
}
